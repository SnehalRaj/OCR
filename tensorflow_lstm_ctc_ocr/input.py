def convert_file_name_to_code(fname):
    fname = os.path.basename(fname)
    code = list(fname.split("_")[1])
    code = np.asarray(
        [common.SPACE_INDEX if x == common.SPACE_TOKEN else (ord(x) - common.FIRST_INDEX) for x in list(code)])
    return code


